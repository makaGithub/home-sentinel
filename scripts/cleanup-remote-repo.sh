#!/bin/bash
# –°–∫—Ä–∏–ø—Ç –¥–ª—è –æ—á–∏—Å—Ç–∫–∏ —É–¥–∞–ª–µ–Ω–Ω–æ–≥–æ —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏—è: –æ—Å—Ç–∞–≤–ª—è–µ—Ç —Ç–æ–ª—å–∫–æ –≤–µ—Ç–∫—É main —Å –æ–¥–Ω–∏–º –∫–æ–º–º–∏—Ç–æ–º
# –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ: ./scripts/cleanup-remote-repo.sh

set -e

# –¶–≤–µ—Ç–∞ –¥–ª—è –≤—ã–≤–æ–¥–∞
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

echo -e "${YELLOW}üßπ –û—á–∏—Å—Ç–∫–∞ —É–¥–∞–ª–µ–Ω–Ω–æ–≥–æ —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏—è home-sentinel${NC}"
echo ""

# –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –º—ã –≤ git —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–∏
if ! git rev-parse --git-dir > /dev/null 2>&1; then
    echo -e "${RED}‚ùå –û—à–∏–±–∫–∞: —ç—Ç–æ –Ω–µ git —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–π${NC}"
    exit 1
fi

# –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –µ—Å—Ç—å —É–¥–∞–ª–µ–Ω–Ω—ã–π —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–π
if ! git remote | grep -q origin; then
    echo -e "${RED}‚ùå –û—à–∏–±–∫–∞: –Ω–µ –Ω–∞–π–¥–µ–Ω —É–¥–∞–ª–µ–Ω–Ω—ã–π —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–π 'origin'${NC}"
    exit 1
fi

REMOTE_URL=$(git remote get-url origin)
echo -e "${YELLOW}üìç –£–¥–∞–ª–µ–Ω–Ω—ã–π —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–π: ${REMOTE_URL}${NC}"
echo ""

# –ü—Ä–æ–≤–µ—Ä—è–µ–º —Ç–µ–∫—É—â—É—é –≤–µ—Ç–∫—É
CURRENT_BRANCH=$(git branch --show-current)
if [ "$CURRENT_BRANCH" != "main" ]; then
    echo -e "${RED}‚ùå –û—à–∏–±–∫–∞: –≤—ã –Ω–µ –Ω–∞ –≤–µ—Ç–∫–µ 'main'${NC}"
    echo "   –¢–µ–∫—É—â–∞—è –≤–µ—Ç–∫–∞: $CURRENT_BRANCH"
    echo "   –ü–µ—Ä–µ–∫–ª—é—á–∏—Ç–µ—Å—å –Ω–∞ main: git checkout main"
    exit 1
fi

# –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –∫–æ–º–º–∏—Ç–æ–≤
COMMIT_COUNT=$(git rev-list --count HEAD)
if [ "$COMMIT_COUNT" -ne 1 ]; then
    echo -e "${YELLOW}‚ö†Ô∏è  –ü—Ä–µ–¥—É–ø—Ä–µ–∂–¥–µ–Ω–∏–µ: –≤ –≤–µ—Ç–∫–µ main –Ω–µ –æ–¥–∏–Ω –∫–æ–º–º–∏—Ç (–Ω–∞–π–¥–µ–Ω–æ: $COMMIT_COUNT)${NC}"
    echo "   –£–±–µ–¥–∏—Ç–µ—Å—å, —á—Ç–æ –≤—ã –≤—ã–ø–æ–ª–Ω–∏–ª–∏ –ª–æ–∫–∞–ª—å–Ω—É—é –æ—á–∏—Å—Ç–∫—É –ø–µ—Ä–µ–¥ –∑–∞–ø—É—Å–∫–æ–º —ç—Ç–æ–≥–æ —Å–∫—Ä–∏–ø—Ç–∞"
    read -p "   –ü—Ä–æ–¥–æ–ª–∂–∏—Ç—å? (y/N): " -n 1 -r
    echo
    if [[ ! $REPLY =~ ^[Yy]$ ]]; then
        exit 1
    fi
fi

# –ü–æ–ª—É—á–∞–µ–º —Å–ø–∏—Å–æ–∫ —É–¥–∞–ª–µ–Ω–Ω—ã—Ö –≤–µ—Ç–æ–∫
REMOTE_BRANCHES=$(git branch -r | grep -v HEAD | sed 's/origin\///' | tr '\n' ' ')
echo -e "${YELLOW}üìã –ù–∞–π–¥–µ–Ω—ã —É–¥–∞–ª–µ–Ω–Ω—ã–µ –≤–µ—Ç–∫–∏:${NC}"
for branch in $REMOTE_BRANCHES; do
    echo "   - $branch"
done
echo ""

# –ü—Ä–µ–¥—É–ø—Ä–µ–∂–¥–µ–Ω–∏–µ
echo -e "${RED}‚ö†Ô∏è  –í–ù–ò–ú–ê–ù–ò–ï: –≠—Ç–æ –Ω–µ–æ–±—Ä–∞—Ç–∏–º–∞—è –æ–ø–µ—Ä–∞—Ü–∏—è!${NC}"
echo ""
echo "   –ë—É–¥–µ—Ç –≤—ã–ø–æ–ª–Ω–µ–Ω–æ:"
echo "   1. Force push –≤–µ—Ç–∫–∏ main (–ø–µ—Ä–µ–∑–∞–ø–∏—à–µ—Ç –≤—Å—é –∏—Å—Ç–æ—Ä–∏—é)"
echo "   2. –£–¥–∞–ª–µ–Ω–∏–µ –≤—Å–µ—Ö –¥—Ä—É–≥–∏—Ö –≤–µ—Ç–æ–∫ –≤ —É–¥–∞–ª–µ–Ω–Ω–æ–º —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–∏"
echo ""
echo -e "${RED}   –í—Å—è –∏—Å—Ç–æ—Ä–∏—è –∫–æ–º–º–∏—Ç–æ–≤ –±—É–¥–µ—Ç —É–¥–∞–ª–µ–Ω–∞!${NC}"
echo ""

read -p "–ü—Ä–æ–¥–æ–ª–∂–∏—Ç—å? (yes/no): " -r
if [[ ! $REPLY =~ ^[Yy][Ee][Ss]$ ]]; then
    echo "–û—Ç–º–µ–Ω–µ–Ω–æ"
    exit 0
fi

echo ""
echo -e "${GREEN}üì§ –®–∞–≥ 1: Force push –≤–µ—Ç–∫–∏ main...${NC}"
git push -f origin main
echo -e "${GREEN}‚úÖ –í–µ—Ç–∫–∞ main –æ–±–Ω–æ–≤–ª–µ–Ω–∞${NC}"
echo ""

# –£–¥–∞–ª—è–µ–º –¥—Ä—É–≥–∏–µ –≤–µ—Ç–∫–∏
echo -e "${GREEN}üóëÔ∏è  –®–∞–≥ 2: –£–¥–∞–ª–µ–Ω–∏–µ –¥—Ä—É–≥–∏—Ö –≤–µ—Ç–æ–∫...${NC}"
for branch in $REMOTE_BRANCHES; do
    if [ "$branch" != "main" ]; then
        echo "   –£–¥–∞–ª–µ–Ω–∏–µ: $branch"
        git push origin --delete "$branch" 2>/dev/null || echo "     (–≤–µ—Ç–∫–∞ —É–∂–µ —É–¥–∞–ª–µ–Ω–∞ –∏–ª–∏ –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç)"
    fi
done
echo -e "${GREEN}‚úÖ –î—Ä—É–≥–∏–µ –≤–µ—Ç–∫–∏ —É–¥–∞–ª–µ–Ω—ã${NC}"
echo ""

# –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º main –∫–∞–∫ –≤–µ—Ç–∫—É –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é
echo -e "${GREEN}üîß –®–∞–≥ 3: –£—Å—Ç–∞–Ω–æ–≤–∫–∞ main –∫–∞–∫ –≤–µ—Ç–∫–∏ –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é...${NC}"
git push origin --set-upstream main 2>/dev/null || true
echo -e "${GREEN}‚úÖ –í–µ—Ç–∫–∞ main —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∞ –∫–∞–∫ –≤–µ—Ç–∫–∞ –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é${NC}"
echo ""

echo -e "${GREEN}‚úÖ –û—á–∏—Å—Ç–∫–∞ –∑–∞–≤–µ—Ä—à–µ–Ω–∞ —É—Å–ø–µ—à–Ω–æ!${NC}"
echo ""
echo "üìä –†–µ–∑—É–ª—å—Ç–∞—Ç:"
echo "   - –£–¥–∞–ª–µ–Ω–Ω—ã–π —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–π —Å–æ–¥–µ—Ä–∂–∏—Ç —Ç–æ–ª—å–∫–æ –≤–µ—Ç–∫—É 'main'"
echo "   - –í –≤–µ—Ç–∫–µ main –æ–¥–∏–Ω –∫–æ–º–º–∏—Ç"
echo "   - –í—Å—è –∏—Å—Ç–æ—Ä–∏—è —É–¥–∞–ª–µ–Ω–∞"
echo ""

